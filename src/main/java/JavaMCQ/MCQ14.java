package JavaMCQ;

public class MCQ14 {
    public static void main(String[] args) {
        char ch = 'B';
        switch (ch) {
            case 'A':
                System.out.print("Apple ");
            case 'B':
                System.out.print("Ball ");
            default:
                System.out.print("Cat ");
        }
    }
}
